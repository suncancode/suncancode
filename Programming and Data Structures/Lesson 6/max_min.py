# max_min.py
# Demonstrates max and min functions

num1 = 1.5
num2 = 5
num3 = 3

min_num = min(num1, num2, num3)
max_num = max(num1, num2, num3)

print("min of {0}, {1}, {2} is {3}".format(num1, num2, num3, min_num))  # min of 1.5, 5, 3 is 1.5
print("max of {0}, {1}, {2} is {3}".format(num1, num2, num3, max_num))  # max of 1.5, 5, 3 is 5