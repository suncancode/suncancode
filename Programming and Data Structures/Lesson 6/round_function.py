# round_function.py
# Demonstrates rounding function with different digits

number = 28.30188679245283

print(round(number))  # 28
print(round(number, 1))  # 28.3
print(round(number, 2))  # 28.3
print(round(number, 3))  # 28.302
print(round(number, 4))  # 28.3019
print(round(number, 5))  # 28.30189
print(round(number, 6))  # 28.301887