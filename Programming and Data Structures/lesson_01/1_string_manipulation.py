# string_manipulation.py
# demonstrate string manipulation in Python

# string concatenation
first_name = "Trung Duc"
last_name = "Le"
full_name = first_name + " " + last_name   
print("My name is" + full_name +  ".")
# output: My name is Trung Duc Le.

# string  repetition
dummy_string = "hehe" * 5
print("Repeated string:", dummy_string)
# output: hehehehehehehehehehe
