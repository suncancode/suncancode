# Practice with basic list oprerations

# Create a list of animals
animal_list = ["dog", "cat", "frog"]

# Access items via index
print("First animal: ", animal_list[0])

# Update list items
animal_list[0] = "bird"
print("Updated list :", animal_list)

# Append new items
animal_list.append("koala")
print("Updated list :", animal_list)

# Get length of list 
length = len(animal_list)
print("Length of list: ", length)